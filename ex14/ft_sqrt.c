int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb > 0 && nb < 2147483647)
	{
		while (i <= (nb / 2 + 1))
		{
			if (i * i == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
